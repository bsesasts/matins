clc;clear all;close all;
fs = 100;N = 50;
t = 0:1/fs:50/fs-1/fs;
f1 = 13.1;f2 = 11.1;
x1 = sin(2*pi*f1*t);
x2 = sin(2*pi*f2*t);
y1 = fft(x1,N);
y2 = fft(x2,N);
fx1 = -fs/2:fs/N:fs/2-fs/N;
figure;
p = 1;
suptitle('Zero-padding in the time domain');
subplot(221);hold on;grid on;
stem(fx1,abs(fftshift(y1/max(y1))),'.');
plot(fx1,abs(fftshift(y1/max(y1))));
stem(fx1,abs(fftshift(y2/max(y2))),'.');
plot(fx1,abs(fftshift(y2/max(y2))));
% plot(fx1,p*(abs(fftshift(y1/max(y1)))+ abs(fftshift(y2/max(y2)))),'-.');
text(-49,0.5,'fs100Ta0.5N50');
legend('dot s1','line s1','dot s2','line s2','s1 + s2');
